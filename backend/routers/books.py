@router.get("/{book_id}/chapters")
async def book_chapters(book_id: int):
    """
    Return book split into chapters.
    Serves from local DB if cached, otherwise fetches from Gutenberg and caches.

    Splitting strategy:
      1. Try Gutenberg's HTML edition (<div class="chapter"> is semantically
         clean — much better for hierarchical books like War and Peace).
      2. Fall back to regex-based splitting on the plain text otherwise.
    """
    cached = await get_cached_book(book_id)

    if cached and cached.get("text"):
        text = cached["text"]
        meta = {k: v for k, v in cached.items() if k not in ("text", "cached_at", "images")}
    else:
        try:
            meta, text = await _fetch_and_cache(book_id)
        except Exception as e:
            msg = str(e) or type(e).__name__
            raise HTTPException(status_code=404, detail=msg)

    chapters = await _split_with_html_preference(book_id, text)
    return {
        "book_id": book_id,
        "meta": meta,
        "chapters": [{"title": c.title, "text": c.text} for c in chapters],
    }


# In-memory cache of chapter splits — keyed by book_id. Populated on first
# chapter-list request after a server restart; every subsequent request is
# instant. Saves ~5s per request from the HTML fetch.
_chapter_cache: dict[int, list] = {}


async def _split_with_html_preference(book_id: int, text: str):
    """Split a book, preferring HTML-based structure over regex-on-text.

    Gutenberg HTML has explicit `<div class="chapter">` markup that survives
    hierarchical structures (BOOK → CHAPTER nesting) gracefully. Only falls
    back to the text splitter when HTML isn't available or doesn't parse.
    """
    if book_id in _chapter_cache:
        return _chapter_cache[book_id]

    try:
        html = await get_book_html(book_id)
        if html:
            chapters = build_chapters_from_html(html)
            if len(chapters) >= 2:
                _chapter_cache[book_id] = chapters
                return chapters
    except Exception:
        logger.exception("HTML split failed for book %s, falling back to text", book_id)

    chapters = build_chapters(text)
    _chapter_cache[book_id] = chapters
    return chapters


async def _fetch_and_cache(book_id: int) -> tuple[dict, str]:
    meta = await get_book_meta(book_id)
    text = await get_book_text(book_id)
    await save_book(book_id, meta, text)
    return meta, text


# ══════════════════════════════════════════════════════════════════════════════
# Interactive book import — streams progress via Server-Sent Events
# ══════════════════════════════════════════════════════════════════════════════

def _sse(event: str, data: dict) -> str:
    """Format a single SSE message."""
    return f"event: {event}\ndata: {json.dumps(data)}\n\n"


@router.get("/{book_id}/import-stream")
async def import_stream(
    book_id: int,
    target_language: str = Query("", description="Target language for pre-translation (empty = skip)"),
    generate_tts: bool = Query(False, description="Also pre-generate TTS audio for each chunk"),
    user: dict = Depends(get_current_user),
):
    """Stream import progress for a book via Server-Sent Events.

    Stages:
      fetching   — downloading book text from Gutenberg (if not cached)
      splitting  — computing chapter structure
      translating — translating each chapter into target_language
      tts        — generating TTS audio for each chunk of each chapter
      done       — all steps complete

    Idempotent: skips already-cached work (same as preseed_translations.py).
    """
    # Resolve Gemini key once up front
    raw_key = user.get("gemini_key")
    decrypted_key: str | None = decrypt_api_key(raw_key) if raw_key else None

    async def generator() -> AsyncIterator[str]:
        try:
            # ── 1. Fetch book text ─────────────────────────────────────────
            yield _sse("stage", {"stage": "fetching", "message": "Fetching book…"})
            cached = await get_cached_book(book_id)
            if cached and cached.get("text"):
                meta = {k: v for k, v in cached.items()
                        if k not in ("text", "cached_at", "images")}
                text = cached["text"]
                yield _sse("stage", {"stage": "fetching", "message": "Already cached",
                                     "progress": 100})
            else:
                try:
                    meta, text = await _fetch_and_cache(book_id)
                except Exception as e:
                    yield _sse("error", {"stage": "fetching", "message": str(e)})
                    return

            source_language = (meta.get("languages") or ["en"])[0]
            yield _sse("meta", {
                "book_id": book_id,
                "title": meta.get("title", ""),
                "source_language": source_language,
            })

            # ── 2. Split chapters ───────────────────────────────────────────
            yield _sse("stage", {"stage": "splitting", "message": "Splitting chapters…"})
            chapters = build_chapters(text)
            yield _sse("chapters", {
                "total": len(chapters),
                "titles": [c.title or f"Chapter {i + 1}" for i, c in enumerate(chapters)],
            })

            # ── 3. Translate each chapter ───────────────────────────────────
            should_translate = (
                target_language
                and target_language != source_language
                and len(chapters) > 0
            )
            if should_translate:
                # Resolve provider: prefer Gemini if user has a key, else Google (free)
                provider = "gemini" if decrypted_key else "google"
                yield _sse("stage", {
                    "stage": "translating",
                    "message": f"Translating to {target_language} ({provider})…",
                    "total": len(chapters),
                    "provider": provider,
                })
                for i, ch in enumerate(chapters):
                    if not ch.text.strip():
                        yield _sse("progress", {"stage": "translating", "current": i + 1,
                                                "total": len(chapters), "skipped": True})
                        continue

                    existing = await get_cached_translation(book_id, i, target_language)
                    if existing:
                        yield _sse("progress", {"stage": "translating", "current": i + 1,
                                                "total": len(chapters), "cached": True,
                                                "title": ch.title})
                        continue

                    try:
                        paragraphs = await translate_text(
                            ch.text, source_language, target_language,
                            provider=provider, gemini_key=decrypted_key,
                        )
                    except Exception as e:
                        # Fall back to Google if Gemini hits quota/etc.
                        if provider == "gemini":
                            try:
                                paragraphs = await translate_text(
                                    ch.text, source_language, target_language,
                                    provider="google",
                                )
                            except Exception as e2:
                                yield _sse("progress", {
                                    "stage": "translating", "current": i + 1,
                                    "total": len(chapters), "title": ch.title,
                                    "error": str(e2)[:120],
                                })
                                continue
                        else:
                            yield _sse("progress", {
                                "stage": "translating", "current": i + 1,
                                "total": len(chapters), "title": ch.title,
                                "error": str(e)[:120],
                            })
                            continue

                    await save_translation(book_id, i, target_language, paragraphs)
                    yield _sse("progress", {
                        "stage": "translating", "current": i + 1,
                        "total": len(chapters), "title": ch.title,
                    })

            # ── 4. Pre-generate TTS audio ──────────────────────────────────
            if generate_tts and chapters:
                voice = _pick_edge_voice(source_language)
                yield _sse("stage", {
                    "stage": "tts",
                    "message": f"Generating audio ({voice})…",
                    "total": len(chapters),
                    "provider": "edge",
                    "voice": voice,
                })
                for i, ch in enumerate(chapters):
                    if not ch.text.strip():
                        yield _sse("progress", {"stage": "tts", "current": i + 1,
                                                "total": len(chapters), "skipped": True})
                        continue

                    chunks = chunk_text(ch.text)
                    for chunk_idx, chunk in enumerate(chunks):
                        cached_audio = await get_cached_audio(
                            book_id, i, "edge", voice, chunk_idx
                        )
                        if cached_audio:
                            continue
                        try:
                            audio_bytes, content_type = await synthesize(
                                chunk, source_language, 1.0, provider="edge",
                            )
                            await save_audio(
                                book_id, i, "edge", voice,
                                audio_bytes, content_type, chunk_idx,
                            )
                        except Exception as e:
                            logger.exception("TTS failed for book=%s ch=%s chunk=%s",
                                             book_id, i, chunk_idx)
                            yield _sse("progress", {
                                "stage": "tts", "current": i + 1,
                                "total": len(chapters), "title": ch.title,
                                "error": str(e)[:120],
                            })
                            break  # skip remaining chunks for this chapter

                    yield _sse("progress", {
                        "stage": "tts", "current": i + 1,
                        "total": len(chapters), "title": ch.title,
                    })

            yield _sse("done", {"book_id": book_id})

        except asyncio.CancelledError:
            # Client disconnected — FastAPI will raise this
            raise
        except Exception as e:
            logger.exception("Import stream crashed")
            yield _sse("error", {"stage": "unknown", "message": str(e)})

    return StreamingResponse(
        generator(),
        media_type="text/event-stream",
        headers={
            "Cache-Control": "no-cache",
            "X-Accel-Buffering": "no",  # disable buffering on any reverse proxy
        },
    )
