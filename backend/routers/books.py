@router.get("/popular")
async def popular_books(
    language: str = Query(default="", max_length=20),
    page: int = Query(1, ge=1),
    per_page: int = Query(50, ge=1, le=200),
):
    """Return a paginated slice of the curated popular Gutenberg books manifest.

    The manifest is either the new dict format {language: [books]} produced by
    seed_books.py --collections, or the legacy flat list.
    """
    global _popular_cache
    if _popular_cache is None:
        if not os.path.isfile(_POPULAR_BOOKS_PATH):
            return {"books": [], "total": 0, "page": page, "per_page": per_page}
        with open(_POPULAR_BOOKS_PATH, encoding="utf-8") as f:
            raw = json.load(f)
        # Normalize: ensure list fields are always arrays, never null.
        def _norm(book: dict) -> dict:
            for field in ("authors", "languages", "subjects"):
                if not isinstance(book.get(field), list):
                    book[field] = []
            return book
        if isinstance(raw, dict):
            _popular_cache = {k: [_norm(b) for b in v] for k, v in raw.items()}
        else:
            _popular_cache = [_norm(b) for b in raw]

    if isinstance(_popular_cache, dict):
        books = _popular_cache.get(language, _popular_cache.get("", []))
    else:
        books = (
            [b for b in _popular_cache if language in b.get("languages", [])]
            if language
            else _popular_cache
        )

    total = len(books)
    start = (page - 1) * per_page
    return {
        "books": books[start : start + per_page],
        "total": total,
        "page": page,
        "per_page": per_page,
    }


@router.get("/{book_id}/translation-status")
async def translation_status(book_id: int, target_language: str = Query(..., max_length=20), user: dict | None = Depends(get_optional_user)):
    """Return book-level translation progress for the reader banner.

    Includes:
      - total_chapters (as split by the splitter)
      - translated_chapters (cached in translations table)
      - queue_pending / queue_running / queue_failed (per-book per-lang
        counts from the translation_queue table) — lets the reader show
        "3/21 chapters still processing" during a background run.
      - bulk_active: legacy one-shot bulk job, kept for back-compat.
    """
    import aiosqlite
    from services.db import count_translations_for_book, DB_PATH

    target_language = target_language.lower().split("-")[0]
    cached = await get_cached_book(book_id)
    if not cached:
        raise HTTPException(status_code=404, detail="Book not found")
    check_book_access(cached, user)
    total_chapters = 0
    if cached.get("text") or cached.get("source") == "upload":
        # Use the shared resolver so total_chapters matches what the reader
        # displays (and what the queue worker processes). Uploaded books now
        # store chapters in the user_book_chapters table (books.text is empty).
        from services.book_chapters import split_with_html_preference
        chapters = await split_with_html_preference(book_id, cached.get("text") or "")
        total_chapters = len(chapters)

    cached_translations = await count_translations_for_book(book_id, target_language)

    # Pull queue stats for this (book, language) in one grouped query.
    queue_counts: dict[str, int] = {}
    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            """SELECT status, COUNT(*) FROM translation_queue
               WHERE book_id=? AND target_language=?
               GROUP BY status""",
            (book_id, target_language),
        ) as cursor:
            async for row in cursor:
                queue_counts[row[0]] = row[1]

    return {
        "book_id": book_id,
        "target_language": target_language,
        "total_chapters": total_chapters,
        "translated_chapters": cached_translations,
        "queue_pending": queue_counts.get("pending", 0),
        "queue_running": queue_counts.get("running", 0),
        "queue_failed": queue_counts.get("failed", 0),
        "queue_done": queue_counts.get("done", 0),
    }


@router.get("/{book_id}/chapters/{chapter_index}/queue-status")
async def chapter_queue_status(
    book_id: int = Path(..., ge=1),
    chapter_index: int = Path(..., ge=0),
    target_language: str = Query(..., max_length=20),
    user: dict = Depends(get_current_user),
):
    """Per-chapter queue lookup for the reader page.

    Any logged-in user can call this — the reader needs it before firing an
    on-demand translate to avoid duplicating work the background worker is
    already scheduled to do.
    """
    target_language = target_language.lower().split("-")[0]
    book = await get_cached_book(book_id)
    if not book:
        raise HTTPException(status_code=404, detail="Book not found")
    check_book_access(book, user)
    from services.book_chapters import split_with_html_preference as _split
    _chapters = await _split(book_id, book.get("text") or "")
    if chapter_index < 0 or chapter_index >= len(_chapters):
        raise HTTPException(
            status_code=400,
            detail=f"Chapter index out of range (book has {len(_chapters)} chapter(s)).",
        )
    from services.translation_queue import queue_status_for_chapter
    return await queue_status_for_chapter(book_id, chapter_index, target_language)


