def _sse(event: str, data: dict) -> str:
    """Format a single SSE message."""
    return f"event: {event}\ndata: {json.dumps(data)}\n\n"


@router.get("/{book_id}/import-stream")
async def import_stream(
    book_id: int,
    user: dict | None = Depends(get_optional_user),
):
    """Stream import progress for a book via Server-Sent Events.

    Stages:
      fetching   — downloading book text from Gutenberg (if not cached)
      splitting  — computing chapter structure
      done       — all steps complete

    Translation is NOT started here. After import the client shows a cost-
    confirmation panel; the user explicitly enqueues via POST
    /books/{id}/translations/enqueue-all.

    Idempotent: skips already-cached work.
    Gutenberg books are publicly accessible; uploaded books require ownership.
    """
    # Access guard runs before the stream starts so the HTTP status is 403,
    # not a mid-stream error. Uploaded books are private; Gutenberg books are
    # not yet in the DB at this point so no check is needed for them.
    _pre_cached = await get_cached_book(book_id)
    if _pre_cached:
        check_book_access(_pre_cached, user)

    async def generator() -> AsyncIterator[str]:
        try:
            # ── 1. Fetch book text ─────────────────────────────────────────
            yield _sse("stage", {"stage": "fetching", "message": "Fetching book…"})
            cached = await get_cached_book(book_id)
            if cached and cached.get("text"):
                meta = {k: v for k, v in cached.items()
                        if k not in ("text", "cached_at", "images")}
                text = cached["text"]
                yield _sse("stage", {"stage": "fetching", "message": "Already cached",
                                     "progress": 100})
            else:
                try:
                    meta, text = await _fetch_and_cache(book_id)
                except Exception as e:
                    yield _sse("error", {"stage": "fetching", "message": str(e)})
                    return

            source_language = (meta.get("languages") or ["en"])[0]
            yield _sse("meta", {
                "book_id": book_id,
                "title": meta.get("title", ""),
                "source_language": source_language,
            })

            # ── 2. Split chapters ───────────────────────────────────────────
            yield _sse("stage", {"stage": "splitting", "message": "Splitting chapters…"})
            from services.book_chapters import split_with_html_preference
            chapters = await split_with_html_preference(book_id, text)
            total_words = sum(len(c.text.split()) for c in chapters)
            yield _sse("chapters", {
                "total": len(chapters),
                "total_words": total_words,
                "titles": [c.title or f"Chapter {i + 1}" for i, c in enumerate(chapters)],
            })

            yield _sse("done", {"book_id": book_id})

        except asyncio.CancelledError:
            # Client disconnected — FastAPI will raise this
            raise
        except Exception as e:
            logger.exception("Import stream crashed")
            yield _sse("error", {"stage": "unknown", "message": str(e)})

    return StreamingResponse(
        generator(),
        media_type="text/event-stream",
        headers={
            "Cache-Control": "no-cache",
            "X-Accel-Buffering": "no",  # disable buffering on any reverse proxy
        },
    )
