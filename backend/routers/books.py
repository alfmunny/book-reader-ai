class RequestTranslationBody(BaseModel):
    target_language: str = Field(..., max_length=20)


@router.post("/{book_id}/chapters/{chapter_index}/translation")
async def request_chapter_translation(
    req: RequestTranslationBody,
    book_id: int = Path(..., ge=1),
    chapter_index: int = Path(..., ge=0),
    user: dict | None = Depends(get_optional_user),
):
    """Unified reader-side translation endpoint.

    Reader page calls this when the user enables Translate for a chapter.
    Three outcomes, returned in a single response shape:

    - status="ready" → the translation is already cached, paragraphs returned
      (served to anyone — no auth required for cache hits)
    - status="pending" / "running" → already in the queue, reader polls
    - If not yet queued, this call enqueues it with a high priority
      (user actively waiting) and returns status="pending" with position.
      Login is required to enqueue new translations.
    """
    from services.db import get_cached_translation_with_meta
    from services.translation_queue import (
        enqueue, queue_status_for_chapter, worker,
    )

    target_language = req.target_language.lower().split("-")[0]

    # 0. Check book existence and access first — uploaded books are private.
    book_meta = await get_cached_book(book_id)
    if book_meta:
        check_book_access(book_meta, user)

    # 0a. Already cached? Served to owner/admin after access check.
    cached = await get_cached_translation_with_meta(
        book_id, chapter_index, target_language,
    )
    if cached:
        return {
            "status": "ready",
            "paragraphs": cached["paragraphs"],
            "provider": cached.get("provider"),
            "model": cached.get("model"),
            "title_translation": cached.get("title_translation"),
        }

    # 0b. Book must exist before we can enqueue a new translation.
    if not book_meta:
        raise HTTPException(status_code=404, detail="Book not found")

    # 0c. Guard: reject draft uploaded books — chapters not yet confirmed.
    if book_meta.get("source") == "upload":
        from services.db import count_draft_user_book_chapters
        if await count_draft_user_book_chapters(book_id) > 0:
            raise HTTPException(
                status_code=400,
                detail="Book chapters not yet confirmed. Confirm the chapter structure before translating.",
            )

    # 0d. Guard: reject same-language translation.
    source = (book_meta.get("languages") or [None])[0]
    if source and source.lower().split("-")[0] == target_language:
        raise HTTPException(
            status_code=400,
            detail="Cannot translate to the same language as the original.",
        )

    # 0e. Guard: reject out-of-bounds chapter_index.
    from services.book_chapters import split_with_html_preference
    _chapters = await split_with_html_preference(book_id, book_meta.get("text") or "")
    if chapter_index < 0 or chapter_index >= len(_chapters):
        raise HTTPException(
            status_code=400,
            detail=f"Chapter index out of range (book has {len(_chapters)} chapter(s)).",
        )

    # 1. New translation requires login.
    if user is None:
        raise HTTPException(
            status_code=401,
            detail="Login required to translate this chapter.",
        )

    # 3. Already queued / running?
    qstatus = await queue_status_for_chapter(
        book_id, chapter_index, target_language,
    )
    if qstatus["queued"]:
        return {
            "status": qstatus["status"],  # 'pending' or 'running'
            "position": qstatus["position"],
            "attempts": qstatus["attempts"],
        }

    # 4. Require user's own Gemini key — do not drain the admin queue key.
    raw_key = user.get("gemini_key")
    try:
        decrypted_key = decrypt_api_key(raw_key) if raw_key else None
    except HTTPException:
        decrypted_key = None
    if not decrypted_key:
        raise HTTPException(
            status_code=403,
            detail="A Gemini API key is required to translate chapters. Please add one in your profile settings.",
        )

    # 5. Not yet queued — enqueue with high priority. Reader-initiated
    # enqueues jump ahead of admin auto-enqueue (priority=100) and
    # admin per-book enqueue (priority=50) because a user is watching.
    queued_by = user.get("email") or user.get("name") or f"user#{user.get('id')}"
    await enqueue(
        book_id, chapter_index, target_language,
        priority=10,
        queued_by=queued_by,
    )
    worker().wake()
    fresh = await queue_status_for_chapter(
        book_id, chapter_index, target_language,
    )
    return {
        "status": fresh["status"],
        "position": fresh["position"],
        "attempts": 0,
    }


@router.post("/{book_id}/translations/enqueue-all")
async def enqueue_all_chapters(
    book_id: int,
    req: RequestTranslationBody,
    user: dict = Depends(get_current_user),
):
    """Queue every not-yet-translated chapter of a book for one language.

    Reader-side counterpart to the admin panel's per-book + Translate
    button. Accepts any authenticated user; queued items use
    priority=20 — above auto-enqueue (100) and admin per-book (50) so
    the user who clicked is served ahead of background rescans, but
    below the active reader's priority=10 so the chapter they're
    currently staring at still wins.
    """
    target_language = req.target_language.lower().split("-")[0]
    book_meta = await get_cached_book(book_id)
    if not book_meta:
        raise HTTPException(status_code=404, detail="Book not found")
    check_book_access(book_meta, user)
    source = (book_meta.get("languages") or [None])[0]
    if source and source.lower().split("-")[0] == target_language:
        raise HTTPException(
            status_code=400,
            detail="Cannot translate to the same language as the original.",
        )

    from services.translation_queue import enqueue_for_book, worker

    queued_by = user.get("email") or user.get("name") or f"user#{user.get('id')}"
    enqueued = await enqueue_for_book(
        book_id,
        target_languages=[target_language],
        priority=20,
        queued_by=queued_by,
    )
    worker().wake()
    return {"ok": True, "enqueued": enqueued}


@router.post("/{book_id}/chapters/{chapter_index}/translation/retry")
async def retry_chapter_translation(
    req: RequestTranslationBody,
    book_id: int = Path(..., ge=1),
    chapter_index: int = Path(..., ge=0),
    user: dict = Depends(get_current_user),
):
    """Re-queue a single chapter whose queue row is in 'failed' state.

    Distinct from `request_chapter_translation` because that endpoint uses
    INSERT OR IGNORE — a failed row would stay failed forever. Retry is an
    explicit action: admin or reader clicks a button. Resets the queue row
    to pending + attempts=0 and bumps priority to 10 (reader-initiated).
    """
    book = await get_cached_book(book_id)
    if not book:
        raise HTTPException(status_code=404, detail="Book not found")
    check_book_access(book, user)

    from services.translation_queue import enqueue, queue_status_for_chapter, worker
    from services.book_chapters import split_with_html_preference as _split

    target_language = req.target_language.lower().split("-")[0]

    _ch = await _split(book_id, book.get("text") or "")
    if chapter_index < 0 or chapter_index >= len(_ch):
        raise HTTPException(
            status_code=400,
            detail=f"Chapter index out of range (book has {len(_ch)} chapter(s)).",
        )

    # Guard: reject retry of a running item. enqueue(reset_failed=True) resets
    # status='pending' in-place — same row ID. When the worker finishes it calls
    # mark_queue_row_done which DELETEs by (book,ch,lang), silently eating the
    # retry. Consistent with the admin queue_retry_item guard (PR #295, issue #294).
    current = await queue_status_for_chapter(book_id, chapter_index, target_language)
    if current.get("status") == "running":
        raise HTTPException(
            status_code=409,
            detail="Cannot retry a running translation; wait for it to finish or fail",
        )

    queued_by = user.get("email") or user.get("name") or f"user#{user.get('id')}"
    await enqueue(
        book_id, chapter_index, target_language,
        priority=10,
        reset_failed=True,
        queued_by=queued_by,
    )
    worker().wake()
    fresh = await queue_status_for_chapter(
        book_id, chapter_index, target_language,
    )
    return {
        "status": fresh["status"],
        "position": fresh["position"],
        "attempts": 0,
    }


