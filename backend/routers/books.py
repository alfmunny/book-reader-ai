@router.get("/{book_id}/chapters")
async def book_chapters(book_id: int, user: dict | None = Depends(get_optional_user)):
    """
    Return book split into chapters.
    Serves from local DB if cached, otherwise fetches from Gutenberg and caches.

    Splitting strategy:
      1. Try Gutenberg's HTML edition (<div class="chapter"> is semantically
         clean — much better for hierarchical books like War and Peace).
      2. Fall back to regex-based splitting on the plain text otherwise.
    """
    cached = await get_cached_book(book_id)

    # Handle uploaded books (chapters in the user_book_chapters table).
    if cached and cached.get("source") == "upload":
        check_book_access(cached, user)
        from services.db import count_draft_user_book_chapters, get_user_book_chapters
        if await count_draft_user_book_chapters(book_id) > 0:
            raise HTTPException(status_code=400, detail="Book not yet confirmed. Visit /upload/{book_id}/chapters to confirm.")
        chapters_rows = await get_user_book_chapters(book_id, include_drafts=False)
        meta = {k: v for k, v in cached.items() if k not in ("text", "cached_at", "images")}
        return {
            "book_id": book_id,
            "meta": meta,
            "chapters": [{"title": r["title"], "text": r["text"]} for r in chapters_rows],
        }

    if cached and cached.get("text"):
        text = cached["text"]
        meta = {k: v for k, v in cached.items() if k not in ("text", "cached_at", "images")}
    else:
        try:
            meta, text = await _fetch_and_cache(book_id)
        except Exception as e:
            msg = str(e) or type(e).__name__
            raise HTTPException(status_code=404, detail=msg)

    from services.book_chapters import split_with_html_preference
    chapters = await split_with_html_preference(book_id, text)
    return {
        "book_id": book_id,
        "meta": meta,
        "chapters": [{"title": c.title, "text": c.text} for c in chapters],
    }


async def _fetch_and_cache(book_id: int) -> tuple[dict, str]:
    meta = await get_book_meta(book_id)
    text = await get_book_text(book_id)
    await save_book(book_id, meta, text)
    # Fetch EPUB in background so book-add stays snappy.
    asyncio.create_task(_fetch_and_store_epub(book_id))
    return meta, text


async def _fetch_and_store_epub(book_id: int) -> None:
    try:
        result = await get_book_epub(book_id)
        if result:
            epub_bytes, epub_url = result
            await save_book_epub(book_id, epub_bytes, epub_url)
            logger.info("EPUB cached for book %d (%d KB)", book_id, len(epub_bytes) // 1024)
    except Exception:
        logger.debug("EPUB fetch failed for book %d (no EPUB edition available)", book_id)


